"""
Gmsh + OpenFOAM case emission for exported high-fidelity cases.

Turns an exported case (wall contour + freestream conditions, as written by
export_high_fidelity_scaffold.py) into:

    gmsh/duct2d.geo        — parametric structured 2-D duct mesh
                             (transfinite quads, one-cell z-extrusion,
                             named Physical groups), MSH2-ready
    openfoam_case/         — rhoCentralFoam case skeleton
                             (0/, constant/, system/, Allrun.sh)

Everything emitted is a TEMPLATE for the screening-level inviscid case:
review schemes/BCs against your pinned OpenFOAM version (research plan §5
recommends ESI v2606) before running, and switch walls to no-slip +
turbulence model for the viscous studies. The geometry, freestream state,
and QoI definitions are the same ones the low-fidelity solver used, which
is the point: apples-to-apples comparisons.

Workflow (research plan §6):
    gmsh -3 duct2d.geo -format msh2 -o duct2d.msh
    gmshToFoam duct2d.msh -case openfoam_case   (then Allrun.sh does both)
"""
from pathlib import Path

import numpy as np


# Gmsh geometry

def write_gmsh_duct(path, x_wall, y_wall, nx_cells=200, ny_cells=60,
                    wall_bump=0.15, dz=0.01):
    """
    Write a structured 2-D planar duct .geo file.

    Args:
        path:      output .geo path
        x_wall:    wall-contour x samples (increasing) [m]
        y_wall:    upper-wall height h(x) [m] (lower wall at y=0)
        nx_cells:  streamwise cell count
        ny_cells:  wall-normal cell count
        wall_bump: transfinite Bump coefficient (<1 clusters at both walls)
        dz:        one-cell z-extrusion thickness [m]
    """
    x_wall = np.asarray(x_wall, dtype=float)
    y_wall = np.asarray(y_wall, dtype=float)
    if len(x_wall) < 4 or np.any(np.diff(x_wall) <= 0):
        raise ValueError("x_wall must be increasing with >= 4 samples")
    if np.any(y_wall <= 0):
        raise ValueError("wall height must stay positive")

    lines = [
        "// duct2d.geo — generated by experiments/gmsh_openfoam_export.py",
        "// Structured 2-D duct: bottom wall y=0, top wall = exported contour.",
        "// Build:  gmsh -3 duct2d.geo -format msh2 -o duct2d.msh",
        "//         (MSH2 ASCII: gmshToFoam does not reliably read MSH4)",
        "",
        f"nx = {int(nx_cells)};",
        f"ny = {int(ny_cells)};",
        f"dz = {dz};",
        "",
        "// corner + contour points",
        f"Point(1) = {{{x_wall[0]:.9g}, 0, 0}};",
        f"Point(2) = {{{x_wall[-1]:.9g}, 0, 0}};",
    ]
    top_ids = []
    for k, (xk, yk) in enumerate(zip(x_wall, y_wall)):
        pid = 101 + k
        top_ids.append(pid)
        lines.append(f"Point({pid}) = {{{xk:.9g}, {yk:.9g}, 0}};")

    id_list = ", ".join(str(p) for p in top_ids)
    lines += [
        "",
        "Line(1) = {1, 2};                // bottom wall",
        f"Spline(2) = {{{id_list}}};       // top wall (exported contour)",
        f"Line(3) = {{1, {top_ids[0]}}};   // inlet",
        f"Line(4) = {{2, {top_ids[-1]}}};  // outlet",
        "",
        "Curve Loop(1) = {1, 4, -2, -3};",
        "Plane Surface(1) = {1};",
        "",
        "// structured quad mesh, clustered toward both walls",
        "Transfinite Curve {1, 2} = nx + 1;",
        f"Transfinite Curve {{3, 4}} = ny + 1 Using Bump {wall_bump};",
        f"Transfinite Surface {{1}} = {{1, 2, {top_ids[-1]}, {top_ids[0]}}};",
        "Recombine Surface {1};",
        "",
        "// one-cell extrusion for OpenFOAM's pseudo-2D convention",
        "ex[] = Extrude {0, 0, dz} { Surface{1}; Layers{1}; Recombine; };",
        "",
        "// physical groups — names become OpenFOAM patches via gmshToFoam;",
        "// unnamed entities would be silently dropped",
        'Physical Surface("frontAndBack") = {1, ex[0]};',
        'Physical Surface("bottomWall")   = {ex[2]};',
        'Physical Surface("outlet")       = {ex[3]};',
        'Physical Surface("topWall")      = {ex[4]};',
        'Physical Surface("inlet")        = {ex[5]};',
        'Physical Volume("fluid")         = {ex[1]};',
        "",
    ]
    path = Path(path)
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text("\n".join(lines))
    return path


# OpenFOAM case skeleton

_FOAM_HEADER = """/*--------------------------------*- C++ -*----------------------------------*\\
| Generated by experiments/gmsh_openfoam_export.py — TEMPLATE: review before |
| running; pin one OpenFOAM version (research plan recommends ESI v2606).    |
\\*---------------------------------------------------------------------------*/
FoamFile
{{
    version     2.0;
    format      ascii;
    class       {cls};
    object      {obj};
}}
"""


def _dict_file(cls, obj, body):
    return _FOAM_HEADER.format(cls=cls, obj=obj) + body


def write_openfoam_case(case_dir, freestream, end_time, write_interval,
                        p_back=None):
    """
    Write a minimal rhoCentralFoam case skeleton.

    Args:
        case_dir:    case directory root
        freestream:  dict with u_inf, p_inf, T_inf (SI)
        end_time:    simulated end time [s]
        write_interval: output interval [s]
        p_back:      optional imposed outlet pressure [Pa]; None keeps a
                     supersonic (zeroGradient/inletOutlet) outlet
    """
    case = Path(case_dir)
    for sub in ("0", "constant", "system"):
        (case / sub).mkdir(parents=True, exist_ok=True)

    u = float(freestream["u_inf"])
    p = float(freestream["p_inf"])
    T = float(freestream["T_inf"])

    outlet_p = (f"""    outlet
    {{
        type            fixedValue;
        value           uniform {p_back};
    }}""" if p_back is not None else
                """    outlet
    {
        type            zeroGradient;
    }""")

    (case / "0" / "p").write_text(_dict_file("volScalarField", "p", f"""
dimensions      [1 -1 -2 0 0 0 0];
internalField   uniform {p};

boundaryField
{{
    inlet
    {{
        type            fixedValue;
        value           uniform {p};
    }}
{outlet_p}
    bottomWall      {{ type zeroGradient; }}
    topWall         {{ type zeroGradient; }}
    frontAndBack    {{ type empty; }}
}}
"""))

    (case / "0" / "T").write_text(_dict_file("volScalarField", "T", f"""
dimensions      [0 0 0 1 0 0 0];
internalField   uniform {T};

boundaryField
{{
    inlet
    {{
        type            fixedValue;
        value           uniform {T};
    }}
    outlet          {{ type zeroGradient; }}
    bottomWall      {{ type zeroGradient; }}
    topWall         {{ type zeroGradient; }}
    frontAndBack    {{ type empty; }}
}}
"""))

    (case / "0" / "U").write_text(_dict_file("volVectorField", "U", f"""
dimensions      [0 1 -1 0 0 0 0];
internalField   uniform ({u} 0 0);

boundaryField
{{
    inlet
    {{
        type            fixedValue;
        value           uniform ({u} 0 0);
    }}
    outlet          {{ type zeroGradient; }}
    // slip for the inviscid screening case; switch to noSlip (+ boundary
    // layer resolution and a turbulence model) for viscous studies
    bottomWall      {{ type slip; }}
    topWall         {{ type slip; }}
    frontAndBack    {{ type empty; }}
}}
"""))

    (case / "constant" / "thermophysicalProperties").write_text(_dict_file(
        "dictionary", "thermophysicalProperties", """
thermoType
{
    type            hePsiThermo;
    mixture         pureMixture;
    transport       sutherland;
    thermo          hConst;
    equationOfState perfectGas;
    specie          specie;
    energy          sensibleInternalEnergy;
}

mixture
{
    specie
    {
        molWeight       28.96;
    }
    thermodynamics
    {
        Cp              1004.5;
        Hf              0;
    }
    transport
    {
        As              1.4792e-06;
        Ts              116;
    }
}
"""))

    (case / "constant" / "turbulenceProperties").write_text(_dict_file(
        "dictionary", "turbulenceProperties", """
simulationType  laminar;   // screening baseline; kOmegaSST for RANS studies
"""))

    (case / "system" / "controlDict").write_text(_dict_file(
        "dictionary", "controlDict", f"""
application     rhoCentralFoam;   // Foundation v11+: foamRun -solver shockFluid

startFrom       startTime;
startTime       0;
stopAt          endTime;
endTime         {end_time};
deltaT          1e-9;
writeControl    adjustableRunTime;
writeInterval   {write_interval};
purgeWrite      0;
writeFormat     ascii;
writePrecision  8;
timeFormat      general;
timePrecision   8;
runTimeModifiable true;

adjustTimeStep  yes;
maxCo           0.3;
maxDeltaT       1e-5;
"""))

    (case / "system" / "fvSchemes").write_text(_dict_file(
        "dictionary", "fvSchemes", """
fluxScheme      Kurganov;

ddtSchemes
{
    default         Euler;
}

gradSchemes
{
    default         Gauss linear;
}

divSchemes
{
    default         none;
    div(tauMC)      Gauss linear;
}

laplacianSchemes
{
    default         Gauss linear corrected;
}

interpolationSchemes
{
    default         linear;
    reconstruct(rho) vanLeer;
    reconstruct(U)  vanLeerV;
    reconstruct(T)  vanLeer;
}

snGradSchemes
{
    default         corrected;
}
"""))

    (case / "system" / "fvSolution").write_text(_dict_file(
        "dictionary", "fvSolution", """
solvers
{
    "(rho|rhoU|rhoE)"
    {
        solver          diagonal;
    }

    U
    {
        solver          smoothSolver;
        smoother        GaussSeidel;
        nSweeps         2;
        tolerance       1e-09;
        relTol          0.01;
    }

    "(e|h)"
    {
        $U;
        tolerance       1e-10;
        relTol          0;
    }
}
"""))

    allrun = case / "Allrun.sh"
    allrun.write_text("""#!/bin/sh
# TEMPLATE driver: gmsh mesh -> gmshToFoam -> patch types -> rhoCentralFoam.
# Run inside your pinned OpenFOAM environment (e.g. the v2606 Docker image).
set -e
cd "$(dirname "$0")"

gmsh -3 ../gmsh/duct2d.geo -format msh2 -o duct2d.msh
gmshToFoam duct2d.msh

# gmshToFoam marks every boundary as 'patch'; fix the types it can't infer
foamDictionary constant/polyMesh/boundary -entry entry0/frontAndBack/type -set empty
foamDictionary constant/polyMesh/boundary -entry entry0/bottomWall/type -set wall
foamDictionary constant/polyMesh/boundary -entry entry0/topWall/type -set wall

checkMesh
rhoCentralFoam | tee log.rhoCentralFoam
""")
    allrun.chmod(0o755)
    return case


def emit_gmsh_openfoam(output_dir, wall_rows, freestream, nx_cells=200,
                       ny_cells=60, flow_throughs=8.0, p_back=None):
    """
    Emit gmsh/ + openfoam_case/ under an exported case directory.

    Args:
        output_dir: exported case directory (from export_case)
        wall_rows:  list of {"x", "y_upper_suggested"} rows (exporter format)
        freestream: dict with u_inf, p_inf, T_inf
        nx_cells, ny_cells: structured mesh resolution
        flow_throughs: endTime in convective flow-through times
        p_back:     optional imposed outlet pressure [Pa]
    """
    output_dir = Path(output_dir)
    x = np.array([row["x"] for row in wall_rows], dtype=float)
    y = np.array([row["y_upper_suggested"] for row in wall_rows], dtype=float)

    # thin the contour for a manageable .geo (spline handles the rest)
    stride = max(len(x) // 80, 1)
    idx = np.unique(np.r_[np.arange(0, len(x), stride), len(x) - 1])

    geo_path = write_gmsh_duct(output_dir / "gmsh" / "duct2d.geo",
                               x[idx], y[idx],
                               nx_cells=nx_cells, ny_cells=ny_cells)

    u_inf = float(freestream["u_inf"])
    t_flow = (x[-1] - x[0]) / max(u_inf, 1e-30)
    end_time = flow_throughs * t_flow
    case_dir = write_openfoam_case(output_dir / "openfoam_case", freestream,
                                   end_time=f"{end_time:.6e}",
                                   write_interval=f"{end_time / 20.0:.6e}",
                                   p_back=p_back)
    return geo_path, case_dir
